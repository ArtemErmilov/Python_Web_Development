class Rectangle:

    def __init__(self,l:float,h = None):
        self.l = l
        self.h = h if h else l
       
        
    def perimeter(self):
        return 2*(self.l+self.h)
    
    def square(self):
        return self.l*self.h

p = Rectangle(5,6)

print(p.perimeter())
print(p.square())