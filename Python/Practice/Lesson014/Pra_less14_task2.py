from string import ascii_lowercase

import doctest

def clear_text(text:str):
    """
    >>> clear_text('aqwert') == 'aqwert'
    True
    >>> clear_text('aqweRT') == 'aqwert'
    True
    >>> clear_text('aqwe,.') == 'aqwe'
    True
    >>> clear_text('aqwe—Ñ—ã–≤–∞') == 'aqwe'
    True
    >>> clear_text('aqwert aqweRT aqwe,. aqwe—Ñ—ã–≤–∞') == 'aqwert aqwert aqwe aqwe'
    True
    """
    result = ''
    if text is not None:
        for i in text.lower():
            if i in ascii_lowercase + ' ':
                result +=i
        return result
    raise ValueError('Incorrect text')

